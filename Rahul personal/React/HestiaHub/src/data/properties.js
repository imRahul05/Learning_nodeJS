export const getPropertyById = (id) => {
    return properties.find(property => property.id === id);
};

export const getAllCities = () => {
    const cities = properties.map(property => property.city);
    return [...new Set(cities)];
};

export const getAllStates = () => {
    const states = properties.map(property => property.state);
    return [...new Set(states)];
};

export const getAllPropertyTypes = () => {
    const types = properties.map(property => property.propertyType);
    return [...new Set(types)];
};

export const getMinMaxPrices = () => {
    const prices = properties.map(property => property.price);
    return {
        min: Math.min(...prices),
        max: Math.max(...prices)
    };
};

export const getMinMaxSquareFeet = () => {
    const squareFeet = properties.map(property => property.squareFeet);
    return {
        min: Math.min(...squareFeet),
        max: Math.max(...squareFeet)
    };
};
